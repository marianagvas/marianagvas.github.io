export const embedUrl = (url, autoplay = false, mute = true) => {
  if (!url) return '';
  const videoId = url.split('v=')[1];
  return `https://www.youtube.com/embed/${videoId}?autoplay=${autoplay ? 1 : 0}&amp;mute=${mute ? 1 : 0}&amp;controls=1&amp;origin=https%3A%2F%2Fwww.mariana-vasconcelos.com&amp;playsinline=1&amp;showinfo=0&amp;enablejsapi=1`
}

export const displayBlockElement = (block, height = '500px', autoplay = false, mute = true) => {
  if (block.type === 'video') {
    return `<iframe frameborder="0" allowfullscreen=""
    allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share"
    width="100%" height="${height}"
    title="${(block.title ?? "") + " " + (block.description ?? "")}"
    src="${embedUrl(block.url, autoplay || block?.autoplay, mute)}"></iframe>`
  } else if (block.type === 'image') {
    const imgHtml = `<img width="100%" src="${block.image}" title="${(block.title ?? "") + " " + (block.description ?? "")}"/>`;
    if(block.url) {
      return `<a href="${block.url}" ${block.url.at(0) == "/"?'':'target="_blank"'}>${imgHtml}</a>`
    }
    return imgHtml;
  } else if (block.type === 'card') {
    return ` <div class="v-card v-theme--customLight bg-primary v-card--density-default v-card--variant-flat pa-4" style="display:grid;min-height: 50%;">
        <div class="v-card-item">
          <div class="v-card-item__content">
            <div class="text-h5 mb-4">${block.title}</div>
            <div class="text-h6 mb-4">${block.description}</div>
            <div class="text-caption">${block.details}</div>
          </div>
      </div>`
  }
}
